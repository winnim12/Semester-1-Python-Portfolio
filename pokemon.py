def pokemon_game():
    print("Welcome to Pokemon Evolution!")
    while True :
        print("Select an activity for the day")
        print("""1. Train
2. Gym Battle
3. Rest
4. Quit""")

        global pokemon_level
        if pokemon_level == 0:
            pokemon_name = "Pichu"
        if pokemon_level == 1:
            pokemon_name = "Pikachu"
        if pokemon_level >= 2:
            pokemon_name = "Guitar Pikachu"


        activity = input("(1-4) Activity:")


        if activity == "1":
            print("Your pokemon did 10 pushups today")
            pokemon_level = pokemon_level + 1
            print("You leveled up to level " + str(pokemon_level))

        elif activity == "2":
            outcome = random.randint(1,2)
            if outcome == 1:
                print("You won the battle.")
                pokemon_level = pokemon_level + 2
                print("You are now on level " + str(pokemon_level))
                global wins
                wins = wins + 1
                global mood
                mood = "happy"
                global loss
                if wins > loss :
                    mood = "really happy"
                if wins == loss :
                    mood = "neutral"
            if outcome == 2:
                print("You lost the battle.")
                pokemon_level = pokemon_level - 1
                print("You are now on level " + str(pokemon_level))
                loss = loss + 1
                mood = "sad"
                if loss > wins:
                    mood = "really sad"
                if wins == loss :
                    mood = "neutral"

        elif activity == "3":
            print("You are on level " + str(pokemon_level) + " and your name is " + str(pokemon_name))
            print("You have " + str(wins) + " wins and " + str(loss) + " losses.")
            print(str(pokemon_name) + " is feeling " + str(mood))
            pokemon_image()

        elif activity == "4":
            break
    print("Bye, have a good day!")

